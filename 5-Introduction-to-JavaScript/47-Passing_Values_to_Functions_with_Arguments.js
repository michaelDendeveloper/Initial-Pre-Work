/* I learned that values are passed to functions are arguments.  Arguments are the actual values that are used as input in a function. Parameters are the variables that are placeholders that are to be input into a function.  The parameters are usually defined when the function is defined.
*/
// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

