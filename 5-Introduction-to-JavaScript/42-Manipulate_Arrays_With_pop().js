/* I learned how to use the .pop() function to change data in an array.  .pop() is used to pop a value off the end of an array.  The value that is "popped off" can be stored by assigning the value to a variable.  The .pop() function removes the last element of an array and returns that element
*/
// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

