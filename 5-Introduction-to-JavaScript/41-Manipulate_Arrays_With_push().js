// Example I learned that the push() function is an easy way to append data to an end of an array
/* for multi-dimensional arrays, the square brackets are in the parentheses in the function.  Use dot operator to call the function.
*/
var ourArray = ["Stimpson", "J", "cat"];
ourArray.push(["happy", "joy"]);
// ourArray now equals ["Stimpson", "J", "cat", ["happy", "joy"]]

